const groupAnagrams = (strs) => {
    const map = new Map()
    for(let str of strs){
        const key = str.split('').sort().join('')
        if(!map.has(key)){
            map.set(key, [])
        }
        map.get(key).push(str)
    }
    return [...map.values()]
}

console.log(groupAnagrams(["act","pots","tops","cat","stop","hat"]))